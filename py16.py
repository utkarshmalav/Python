# Arithmetic operator modules with Functions

import operator as op
a=10
b=4

print("Addition",a+b)
print(op.add(a,b))

print("Subtraction",a-b)
print(op.sub(a,b))

print("Multiplication",a*b)
print(op.mul(a,b))

print("Divison true",a/b)
print(op.truediv(a,b))

print("Divison floor",a//b)
print(op.floordiv(a,b))

print("Power of 10 ^ 4",a**b)
print(op.pow(a,b))

print("Modulas",a%b)
print(op.mod(a,b))



